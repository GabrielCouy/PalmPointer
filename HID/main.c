void process_mouse()
{
	if(counter > 1)
	{
		counter = 0;
	}
	mouse.button = 0;
	mouse.mouse_x = 0;
	mouse.mouse_y = 0;
	mouse.wheel = 0;
	if (rx_buffer[0] != 0xAA) {
		HAL_Delay(1);
	}
	else {
	int8_t xR = rx_buffer[2];
	int8_t yR = rx_buffer[1];

	movement = rx_buffer[5];

	if(rx_buffer[3]==1 && rx_buffer[4]==1)
	{
		if(counterWheel > 10)
		{
			counterWheel = 0;
		}
		if(counterWheel ==10)
		{
			mouse.button = 0;
			mouse.mouse_x = 0;
			mouse.mouse_y = 0;
			mouse.wheel =  rx_buffer[1];
			if(mouse.wheel > 1)
			{
				mouse.wheel = 1;
			}else if (mouse.wheel < -1)
			{
				mouse.wheel = -1;
			}
		}
		counterWheel++;
		return;
	}
	counterWheel =0;
	if(rx_buffer[3] ==1)
	{
		mouse.button = 1;
	}else if(rx_buffer[4] == 1)
	{
		mouse.button = 2;
	}else{
		mouse.button = 0;
	}

	if (counter == 1)
	{

		if(xR >= -buff && xR <= buff)
			{
				mouse.mouse_x = 0;
			}else if(xR < -buff){
				if(orientation > 0)
				{
					mouse.mouse_x = (movement/100) *( orientation*xR + buff);
				}else{
					mouse.mouse_x = (movement/100) * (orientation*xR - buff);
				}
			}else{
				if(orientation > 0)
				{
					mouse.mouse_x = (movement/100) *(orientation*xR - buff);
				}else{
					mouse.mouse_x = (movement/100) *( orientation*xR + buff);
				}

			}

			if(yR >= -ybuff && yR  <= ybuff)
			{
				mouse.mouse_y = 0;
			}else if(yR < -ybuff)
			{
				mouse.mouse_y = (movement/100) *(yR +ybuff );
			}else{
				mouse.mouse_y = (movement/100) *(yR - ybuff);
			}
	}


	mouse.wheel  = 0;
	counter ++;
	}

//	mouse.mouse_y = rx_buffer[0]
	//mouse.wheel = rx_buffer[1];


//	mouse.button = rx_buffer[0];
//	mouse.mouse_x = rx_buffer[1];
//	mouse.mouse_y = rx_buffer[2];
	//mouse.wheel = rx_buffer[3];
}

